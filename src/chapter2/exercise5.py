#This program prompts the user to enter a Celsius temperature and converts the Celsius temperature to Fahrenheit and then prints out the converted temperature.

#Prompt the user to enter the temperature in degrees celsius
Celsius = float(input('Enter a temperature in Celsius:'))

#Below is the formula to convert the temperature from degrees celsius to fahrenheit
Fahrenheit = (Celsius * 1.8) + 32


#Print out the temperature in fahrenheit
print('The temperature in Fahrenheit is:', Fahrenheit)